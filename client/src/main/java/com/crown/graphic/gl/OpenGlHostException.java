package com.crown.graphic.gl;

public class OpenGlHostException extends OpenGlException {
    public OpenGlHostException() {
    }

    public OpenGlHostException(String message) {
        super(message);
    }

    public OpenGlHostException(String message, Throwable cause) {
        super(message, cause);
    }

    public OpenGlHostException(Throwable cause) {
        super(cause);
    }

    public OpenGlHostException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
